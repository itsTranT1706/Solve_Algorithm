    public void showTable() throws FileNotFoundException, IOException {
        File f = new File("D:\\JAVA_28TECH\\LuyenDe\\src\\AT18\\De517\\Hanghoa.dat");
        FileReader fr = new FileReader(f);
        BufferedReader br = new BufferedReader(fr);
        String line = br.readLine();
        while(line != null) {
            String[] a = line.split("\\$");
            HangHoa hangHoa = new HangHoa(a[0], a[1], Integer.parseInt(a[2]), Integer.parseInt(a[3]), a[4], a[5]);
            danhSach.add(hangHoa);
            line = br.readLine();
        }
        String[] cols = {"Mã hàng", "Tên hàng", "Số lượng", "Giá tiền", "Ngày sản xuất", "Hạn sử dụng"};
        DefaultTableModel model = new DefaultTableModel(cols, 0);
        for(HangHoa x : danhSach) {
            model.addRow(new Object[] {x.getMaHang(), x.getTenHang(), x.getSoLuong(), x.getGiaTien(), x.getNgaySanXuat(), x.getHanSuDung()});
        }
        tableMain.setModel(model);
    }

