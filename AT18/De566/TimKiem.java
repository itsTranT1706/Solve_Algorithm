    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int luaChon = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn sửa ?");
        if(luaChon == 0) {
            String en = txtEn1.getText();
            String vn = txtVN1.getText();
            int dau = 0, cuoi = arr.size() - 1;
            while(dau <= cuoi) {
                int giua = (dau + cuoi) / 2;
                if(arr.get(giua).getEn().compareToIgnoreCase(en) == 0) {
                    arr.get(giua).setVn(vn);
                    break;
                }
                else if(arr.get(giua).getEn().compareToIgnoreCase(en) > 0)
                    cuoi = giua - 1;
                else if(arr.get(giua).getEn().compareToIgnoreCase(en) < 0)
                    dau = giua + 1;
            }
            JOptionPane.showMessageDialog(this, "Sửa thành công !");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

