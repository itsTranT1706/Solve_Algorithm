    public String timKiemTuanTu(String en) {
        for(Word x : arr) {
            if(x.getEn().equalsIgnoreCase(en)) {
                return x.getVn();
            }   
        }
        return "";
    }
    
    public String timKiemNhiPhan(String en) {
        int dau = 0, cuoi = arr.size() - 1;
        while(dau <= cuoi) {
            int m = (dau + cuoi) / 2;
            if(arr.get(m).getEn().compareToIgnoreCase(en) == 0)
                return arr.get(m).getVn();
            else if(arr.get(m).getEn().compareToIgnoreCase(en) > 0)
                cuoi = m - 1;
            else if(arr.get(m).getEn().compareToIgnoreCase(en) < 0)
                dau = m + 1;
        }
        return "";
    }
    
    public void sapXep() {
        Collections.sort(arr, new Comparator<Word> (){
            @Override
            public int compare(Word o1, Word o2) {
                return o1.getEn().compareToIgnoreCase(o2.getEn());
            }
        });
    }

