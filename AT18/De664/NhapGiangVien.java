    public boolean kiemTraThongTin(GiangVien a) {
        for(GiangVien x : danhSach) {
            if(x.getId().equalsIgnoreCase(a.getId()) || x.getMaGV().equalsIgnoreCase(a.getMaGV()))
                return false;
        }
        return true;
    }
    
    public GiangVien timKiem(String hoTen) {
        int dau = 0, cuoi = danhSach.size() - 1;
        while(dau <= cuoi) {
            int m = (dau + cuoi) / 2;
            if(danhSach.get(m).getHoTen().compareToIgnoreCase(hoTen) == 0)
                return danhSach.get(m);
            else if(danhSach.get(m).getHoTen().compareToIgnoreCase(hoTen) > 0)
                cuoi = m - 1;
            else if(danhSach.get(m).getHoTen().compareToIgnoreCase(hoTen) < 0)
                dau = m + 1;
        }
        return null;
    }
    
    public void sapXep() {
        Collections.sort(danhSach, new Comparator<GiangVien> (){
            @Override
            public int compare(GiangVien o1, GiangVien o2) {
                if(o1.getId().compareToIgnoreCase(o2.getId()) <= 0 && o1.getMaGV().compareToIgnoreCase(o2.getMaGV()) <= 0)
                    return -1;
                else
                    return 1;
            }
        });
    }
    
