    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
        String hoTen = txtHoTen.getText();
        String mGV = txtMGV.getText();
        String diaChi = txtDiaChi.getText();
        String gioiTinh = "";
        if(radiobtnNam.isSelected())
            gioiTinh += "Nam";
        else if(radiobtnNu.isSelected())
            gioiTinh += "Nữ";
        int tmp = comboKhoa.getSelectedIndex();
        String khoa = comboKhoa.getItemAt(tmp);
        GiangVien gv = new GiangVien(id, hoTen, mGV, diaChi, gioiTinh, khoa);
        if(kiemTraThongTin(gv)) {
            danhSach.add(gv);
            try {
                luuFile();
            } catch (IOException ex) {
                Logger.getLogger(NhapGiangVien.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Lưu thành công !");
            xoaThongTin();
            sapXep();
        }
        else {
            JOptionPane.showMessageDialog(this, "Vui lòng thử lại, có vẻ như ID hoặc mã GV bị trùng !");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String hoTen = txtHoTen.getText().trim();
        if(timKiem(hoTen) != null) {
            GiangVien x = timKiem(hoTen);
            txtHoTen.setText(x.getHoTen());
            txtID.setText(x.getId());
            txtMGV.setText(x.getMaGV());
            txtDiaChi.setText(x.getDiaChi());
            if(x.getGioiTinh().equals("Nam"))
                radiobtnNam.setSelected(true);
            else if(x.getGioiTinh().equals("Nữ"))
                radiobtnNu.setSelected(true);
            comboKhoa.setSelectedItem(x.getKhoa());
        }
        else 
            JOptionPane.showMessageDialog(this, "Không tìm thấy !");
    }//GEN-LAST:event_btnTimKiemActionPerformed

